/*基础练习 特殊回文数
问题描述
　　123321是一个非常特殊的数，它从左边读和从右边读是一样的。
　　输入一个正整数n,编程求所有这样的五位和六位十进制数,满足各位数字之和等于n 。
输入格式
　　输入一行，包含一个正整数n。
输出格式
　　按从小到大的顺序输出满足条件的整数，每个整数占一行。
样例输入
52
样例输出
899998
989989
998899
数据规模和约定
　　1<=n<=54。
*/

#include <stdio.h>

int main()
{
	int n,i,j,sum;
	char str[10];
	scanf("%d",&n);
	for (i=10000; i<1000000; i++){
		sprintf(str,"%d",i);
		sum=0;
		for (j=0; str[j]; j++)
			sum += str[j] -'0';
		if (sum != n)
			continue;
		if (i<100000 && str[0]==str[4] && str[1]==str[3])
			printf("%d\n",i);
		if (i>=100000 && str[0]==str[5] && str[1]==str[4] && str[2]==str[3])
			printf("%d\n",i);	
	}
	return 0;
}
